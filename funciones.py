def mostrar_propiedades(nombre, edad, ciudad, peso):
    print(f"Hola mi nombre es {nombre} y tengo {edad} años vivo en {ciudad} y peso {peso}kg")

def calcular_area_rectangulo(base, altura):
    area = base * altura
    print(f"El área del rectángulo es: {area} m2")

def calcular_dias_vividos(edad):
    diasvividos = 365
    total = edad * diasvividos
    print(f"Has vivido un total de {total} días")

def realizar_operaciones(numero1, numero2):
    suma = numero1 + numero2
    resta = numero1 - numero2
    multiplicacion = numero1 * numero2
    print(f"La suma es = {suma}, la resta es = {resta}, la multiplicación es = {multiplicacion}")